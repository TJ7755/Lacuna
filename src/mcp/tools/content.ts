// The content CRUD tool group (Arc 2 §2.3): thin wrappers over src/db/repository.ts,
// exposed to an MCP client behind a one-time write grant per course (§2.4 — a later task
// wires actual grant checking). These are additive or idempotent by construction, so no
// snapshot/undo pairing is needed here (contrast the destructive group, a later task).
//
// `update_card` deliberately accepts only content fields (front/back/tags/flagged) —
// never FSRS/scheduling fields — per src/mcp/registry.ts's documented exclusion. Every
// tool that touches a card also refuses to edit sequence-generated cards (those with a
// `sequenceItemId`), mirroring repository.ts's `assertNoGeneratedCards` convention: such
// cards may only be added, edited or removed via their owning sequence.

import { z } from 'zod';
import { db } from '../../db/schema';
import type { CourseAssessment } from '../../db/types';
import * as read from '../../db/read';
import {
  createCourse as repoCreateCourse,
  updateCourse as repoUpdateCourse,
  createLesson as repoCreateLesson,
  updateLesson as repoUpdateLesson,
  createNote as repoCreateNote,
  updateNote as repoUpdateNote,
  createCourseCard,
  createLessonCard,
  updateCard as repoUpdateCard,
  linkCardToLesson as repoLinkCardToLesson,
  createSequence as repoCreateSequence,
  updateSequence as repoUpdateSequence,
  createCourseAssessment as repoCreateCourseAssessment,
  updateCourseAssessment as repoUpdateCourseAssessment,
} from '../../db/repository';
import { McpToolException, type ToolDefinition, type ToolResult } from '../types';

const courseIdSchema = z.string().describe('The id of the course.');
const lessonIdSchema = z.string().describe('The id of the lesson.');

function ok<T>(data: T): ToolResult<T> {
  return { data };
}

function notFound(kind: string, id: string): never {
  throw new McpToolException({ kind: 'not_found', message: `${kind} "${id}" was not found.` });
}

function refuseGeneratedCard(cardId: string): never {
  throw new McpToolException({
    kind: 'conflict',
    message:
      `Card "${cardId}" was generated by a sequence and can only be edited via that sequence ` +
      '(lacuna.update_sequence), not directly.',
  });
}

// ---------------------------------------------------------------------------
// Courses
// ---------------------------------------------------------------------------

const createCourseSchema = z.object({
  name: z.string().describe('The course name.'),
  description: z.string().optional().describe('Optional free-text description.'),
});
const createCourse: ToolDefinition<
  z.infer<typeof createCourseSchema>,
  Awaited<ReturnType<typeof repoCreateCourse>>
> = {
  name: 'lacuna.create_course',
  description:
    'Create a new course with sensible scheduling defaults (exam date, FSRS parameters, unlock mode).',
  inputSchema: createCourseSchema,
  requiredScope: 'write',
  async handler({ name, description }) {
    return ok(
      await repoCreateCourse(name, description !== undefined ? { description } : undefined),
    );
  },
};

const updateCourseSchema = z.object({
  courseId: courseIdSchema,
  name: z.string().optional().describe('New course name.'),
  description: z.string().optional().describe('New free-text description.'),
  examDate: z
    .number()
    .optional()
    .describe('New primary exam date/time, as an epoch millisecond value in UTC.'),
});
const updateCourse: ToolDefinition<z.infer<typeof updateCourseSchema>, { id: string }> = {
  name: 'lacuna.update_course',
  description: "Update a course's name, description, and/or primary exam date.",
  inputSchema: updateCourseSchema,
  requiredScope: 'write',
  async handler({ courseId, ...changes }) {
    if (!(await read.getCourse(courseId))) notFound('Course', courseId);
    const { examDate, ...courseChanges } = changes;
    if (Object.keys(courseChanges).length > 0) {
      await repoUpdateCourse(courseId, courseChanges);
    }
    if (examDate !== undefined) {
      const finalAssessment = await db.courseAssessments
        .where('courseId')
        .equals(courseId)
        .filter((assessment) => assessment.kind === 'final')
        .first();
      if (!finalAssessment) notFound('Final assessment for course', courseId);
      await repoUpdateCourseAssessment(finalAssessment.id, { examDate });
    }
    return ok({ id: courseId });
  },
};

